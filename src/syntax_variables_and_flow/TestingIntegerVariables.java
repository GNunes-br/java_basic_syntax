package syntax_variables_and_flow;

public class TestingIntegerVariables {
	public static void main(String[] args) {
		System.out.println("Ola novo teste");
		
		int age;
		age = 37;
		
		System.out.println(age);
		
		age = 30 + 10;
		
		System.out.println(age);
		
		age = 7 * 5 + 2;
		
		System.out.println(age);
		
		System.out.println("A idade eh " + age + ", parabens");
		
		int division = 5 / 2;
		
		System.out.println(division);
	}
}
